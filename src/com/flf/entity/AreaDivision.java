package com.flf.entity;

import javax.xml.bind.annotation.XmlRootElement;

//面积划分表
@XmlRootElement(name = "AreaDivision")
public class AreaDivision {
    private String areaDivisionId;

    private String areaDivisionName;

    private String areaDivisionNum;

    private Double unitPrice;

    private Double sum;

    private String productId;
    
    private String areas;

    public String getAreaDivisionId() {
        return areaDivisionId;
    }

    public void setAreaDivisionId(String areaDivisionId) {
        this.areaDivisionId = areaDivisionId;
    }

    public String getAreaDivisionName() {
        return areaDivisionName;
    }

    public void setAreaDivisionName(String areaDivisionName) {
        this.areaDivisionName = areaDivisionName;
    }

    public String getAreaDivisionNum() {
        return areaDivisionNum;
    }

    public void setAreaDivisionNum(String areaDivisionNum) {
        this.areaDivisionNum = areaDivisionNum;
    }

    public Double getUnitPrice() {
        return unitPrice;
    }

    public void setUnitPrice(Double unitPrice) {
        this.unitPrice = unitPrice;
    }

    public Double getSum() {
        return sum;
    }

    public void setSum(Double sum) {
        this.sum = sum;
    }

    public String getProductId() {
        return productId;
    }

    public void setProductId(String productId) {
        this.productId = productId;
    }

	public String getAreas() {
		return areas;
	}

	public void setAreas(String areas) {
		this.areas = areas;
	}
}