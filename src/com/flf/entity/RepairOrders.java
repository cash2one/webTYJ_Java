package com.flf.entity;

import java.util.Date;
import java.util.List;

import javax.xml.bind.annotation.XmlRootElement;

/**
 * 维修单
 * @author wangzhou
 *
 */
@XmlRootElement(name="RepairOrders")
public class RepairOrders {
	
	//工单id
    private String orderId;

    //工单编号
    private String orderNum;

    //工单类型
    private Byte orderType;

    //工单状态
    private Byte orderState;

    //关联任务id
    private String taskId;

    //关联服务请求id
    private String serviceRequestId;

    //操作人id
    private String operatorId;

    //分类id
    private String nodeId;

    //说明
    private String remarks;

    //完成时间
    private Date finishTime;

    //关闭人id
    private String closePersonId;

    //关闭时间
    private Date closeTime;

    //关闭说明
    private String closeRemarks;
  
    //关联附件
    private List<Annex> annexs;
    
    //关联实际消耗资源记录
    private List<ActualConsumptionRecords> actualConsumptionRecords;
    
    //负责人
    private String signature;

    public String getOrderId() {
        return orderId;
    }

    public void setOrderId(String orderId) {
        this.orderId = orderId;
    }

    public String getOrderNum() {
        return orderNum;
    }

    public void setOrderNum(String orderNum) {
        this.orderNum = orderNum;
    }

    public Byte getOrderType() {
        return orderType;
    }

    public void setOrderType(Byte orderType) {
        this.orderType = orderType;
    }

    public Byte getOrderState() {
        return orderState;
    }

    public void setOrderState(Byte orderState) {
        this.orderState = orderState;
    }

    public String getTaskId() {
        return taskId;
    }

    public void setTaskId(String taskId) {
        this.taskId = taskId;
    }

    public String getServiceRequestId() {
        return serviceRequestId;
    }

    public void setServiceRequestId(String serviceRequestId) {
        this.serviceRequestId = serviceRequestId;
    }

    public String getOperatorId() {
        return operatorId;
    }

    public void setOperatorId(String operatorId) {
        this.operatorId = operatorId;
    }

    public String getNodeId() {
        return nodeId;
    }

    public void setNodeId(String nodeId) {
        this.nodeId = nodeId;
    }

    public String getRemarks() {
        return remarks;
    }

    public void setRemarks(String remarks) {
        this.remarks = remarks;
    }

    public Date getFinishTime() {
        return finishTime;
    }

    public void setFinishTime(Date finishTime) {
        this.finishTime = finishTime;
    }

    public String getClosePersonId() {
        return closePersonId;
    }

    public void setClosePersonId(String closePersonId) {
        this.closePersonId = closePersonId;
    }

    public Date getCloseTime() {
        return closeTime;
    }

    public void setCloseTime(Date closeTime) {
        this.closeTime = closeTime;
    }

    public String getCloseRemarks() {
        return closeRemarks;
    }

    public void setCloseRemarks(String closeRemarks) {
        this.closeRemarks = closeRemarks;
    }

	public List<Annex> getAnnexs() {
		return annexs;
	}

	public void setAnnexs(List<Annex> annexs) {
		this.annexs = annexs;
	}

	public List<ActualConsumptionRecords> getActualConsumptionRecords() {
		return actualConsumptionRecords;
	}

	public void setActualConsumptionRecords(
			List<ActualConsumptionRecords> actualConsumptionRecords) {
		this.actualConsumptionRecords = actualConsumptionRecords;
	}

	public String getSignature() {
		return signature;
	}

	public void setSignature(String signature) {
		this.signature = signature;
	}
	
	
}