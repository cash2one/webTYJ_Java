package com.flf.entity;

import java.util.List;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement(name = "SchemeDatails")
// 方案公式收费项目关系表
public class SchemeDatails {
	private String schemeDatailsId; // 关系id

	private String chargeItemId; // 收费项目id

	private String chargeItemName; // 收费项目名称

	private String formulaId; // 公式id

	private String formulaName; // 公式名称

	private String schemeId; // 方案id

	private String chargingFrequency; // 收费频率

	private String buildingId; // 建筑结构id

	private String fullName; // 房屋全名
	
	private List<Formula> formulaList;//关联公式表
	
	private List<String> buildingIdlist;//建筑结果id列表

	public String getBuildingId() {
		return buildingId;
	}

	public void setBuildingId(String buildingId) {
		this.buildingId = buildingId;
	}

	public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	public String getSchemeDatailsId() {
		return schemeDatailsId;
	}

	public void setSchemeDatailsId(String schemeDatailsId) {
		this.schemeDatailsId = schemeDatailsId;
	}

	public String getChargeItemId() {
		return chargeItemId;
	}

	public void setChargeItemId(String chargeItemId) {
		this.chargeItemId = chargeItemId;
	}

	public String getChargeItemName() {
		return chargeItemName;
	}

	public void setChargeItemName(String chargeItemName) {
		this.chargeItemName = chargeItemName;
	}

	public String getFormulaId() {
		return formulaId;
	}

	public void setFormulaId(String formulaId) {
		this.formulaId = formulaId;
	}

	public String getFormulaName() {
		return formulaName;
	}

	public void setFormulaName(String formulaName) {
		this.formulaName = formulaName;
	}

	public String getSchemeId() {
		return schemeId;
	}

	public void setSchemeId(String schemeId) {
		this.schemeId = schemeId;
	}

	public String getChargingFrequency() {
		return chargingFrequency;
	}

	public void setChargingFrequency(String chargingFrequency) {
		this.chargingFrequency = chargingFrequency;
	}

	public List<Formula> getFormulaList() {
		return formulaList;
	}

	public void setFormulaList(List<Formula> formulaList) {
		this.formulaList = formulaList;
	}

	public List<String> getBuildingIdlist() {
		return buildingIdlist;
	}

	public void setBuildingIdlist(List<String> buildingIdlist) {
		this.buildingIdlist = buildingIdlist;
	}
}