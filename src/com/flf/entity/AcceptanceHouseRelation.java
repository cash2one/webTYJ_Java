package com.flf.entity;

import javax.xml.bind.annotation.XmlRootElement;

/**
 * @see <code>验房管理，验房活动记录人员建筑关系</code>
 * @author zhuwei
 * @date 2015-07-17
 */
@XmlRootElement(name = "AcceptanceHouseRelation")
public class AcceptanceHouseRelation {
	
	private String id;//验房活动关系主键id
	private String acceptanceHouseId;//验房活动id
	private String staffId;//团队成员id
	private String buildingStructureId;//建筑结构id
	
	private Staff staff;//团队实体   【查询显示用】
	private BuildingStructureNew buildingStructureNew;//建筑实体  【查询显示用】 
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAcceptanceHouseId() {
		return acceptanceHouseId;
	}
	public void setAcceptanceHouseId(String acceptanceHouseId) {
		this.acceptanceHouseId = acceptanceHouseId;
	}
	public String getStaffId() {
		return staffId;
	}
	public void setStaffId(String staffId) {
		this.staffId = staffId;
	}
	public String getBuildingStructureId() {
		return buildingStructureId;
	}
	public void setBuildingStructureId(String buildingStructureId) {
		this.buildingStructureId = buildingStructureId;
	}
	public Staff getStaff() {
		return staff;
	}
	public void setStaff(Staff staff) {
		this.staff = staff;
	}
	public BuildingStructureNew getBuildingStructureNew() {
		return buildingStructureNew;
	}
	public void setBuildingStructureNew(BuildingStructureNew buildingStructureNew) {
		this.buildingStructureNew = buildingStructureNew;
	}
	
}
