package com.flf.entity;

import javax.xml.bind.annotation.XmlRootElement;

/*
 * 班次类型表
 */
@XmlRootElement(name = "ClassesType")
public class ClassesType {
	private String classesTypeId;

	private String classesTypeName;

	public String getClassesTypeId() {
		return classesTypeId;
	}

	public void setClassesTypeId(String classesTypeId) {
		this.classesTypeId = classesTypeId;
	}

	public String getClassesTypeName() {
		return classesTypeName;
	}

	public void setClassesTypeName(String classesTypeName) {
		this.classesTypeName = classesTypeName;
	}
}