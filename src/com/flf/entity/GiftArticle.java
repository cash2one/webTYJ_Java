package com.flf.entity;

import java.util.List;
import javax.xml.bind.annotation.XmlRootElement;
@XmlRootElement(name = "GiftArticle")
public class GiftArticle {
	
	private String giftArticleId ; //礼包物品关系id
	
	private String giftId ;  //礼包id
	
	private String articleId ; //物品id
	
	private int quantity ; //物品数量 
	
	private Gift gift; //礼包实体
	
	private int state;//领取状态
	
	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

	private HouseArticle houseArticle; //物品实体
	
	private List<HouseArticle> houseArticles; //物品实体集合
	
	private String giftName; //礼品包名称
	
	private Page page;

	public String getGiftName() {
		return giftName;
	}

	public void setGiftName(String giftName) {
		this.giftName = giftName;
	}

	public Page getPage() {
		return page;
	}

	public void setPage(Page page) {
		this.page = page;
	}

	public List<HouseArticle> getHouseArticles() {
		return houseArticles;
	}

	public void setHouseArticles(List<HouseArticle> houseArticles) {
		this.houseArticles = houseArticles;
	}

	public Gift getGift() {
		return gift;
	}

	public void setGift(Gift gift) {
		this.gift = gift;
	}

	public HouseArticle getHouseArticle() {
		return houseArticle;
	}

	public void setHouseArticle(HouseArticle houseArticle) {
		this.houseArticle = houseArticle;
	}

	public String getGiftArticleId() {
		return giftArticleId;
	}

	public void setGiftArticleId(String giftArticleId) {
		this.giftArticleId = giftArticleId;
	}

	public String getGiftId() {
		return giftId;
	}

	public void setGiftId(String giftId) {
		this.giftId = giftId;
	}

	public String getArticleId() {
		return articleId;
	}

	public void setArticleId(String articleId) {
		this.articleId = articleId;
	}

	public int getQuantity() {
		return quantity;
	}

	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}

}
