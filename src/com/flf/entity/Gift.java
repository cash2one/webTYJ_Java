package com.flf.entity;

import java.util.List;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement(name = "Gift")

public class Gift {

	private String giftId; //礼包id
	
	private String giftName; //礼包名称
	
	private String buildingStructureId; //建筑结构id
	
	private int giftAmount; //礼包数量
	
	private List<GiftArticle> giftArticles;//物品集合

	public List<GiftArticle> getGiftArticles() {
		return giftArticles;
	}

	public void setGiftArticles(List<GiftArticle> giftArticles) {
		this.giftArticles = giftArticles;
	}

	private Page page;

	public Page getPage() {
		return page;
	}

	public void setPage(Page page) {
		this.page = page;
	}

	public int getGiftAmount() {
		return giftAmount;
	}

	public void setGiftAmount(int giftAmount) {
		this.giftAmount = giftAmount;
	}

	public String getGiftId() {	
		return giftId;
	}

	public void setGiftId(String giftId) {
		this.giftId = giftId;
	}

	public String getGiftName() {
		return giftName;
	}

	public void setGiftName(String giftName) {
		this.giftName = giftName;
	}

	public String getBuildingStructureId() {
		return buildingStructureId;
	}

	public void setBuildingStructureId(String buildingStructureId) {
		this.buildingStructureId = buildingStructureId;
	}
	
}