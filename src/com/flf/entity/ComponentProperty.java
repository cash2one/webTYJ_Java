package com.flf.entity;

import java.util.List;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement(name = "ComponentProperty") 
public class ComponentProperty {
    private String componentPropertyId;  //组件与资产关联id

    private String componentId;//组件id

    private String propertyId;//资产id

    private Double propertyNumber;//数量

    private String propertyArea;//单位

    private Property property; //资产对象
    
    private String functionalId ; //功能区id
    
    private Page page;

    private String functionalName;//功能区名称
    
    private String propertyName;//资产名称    

	private String fullName;   //建筑结构全名
    
    private String buildingStructureId; //建筑结构id

    private Functional functionals;		//功能区间
    
    private List<PropertyType> propertytype;	//关联专业
    
    
    public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	public String getBuildingStructureId() {
		return buildingStructureId;
	}

	public void setBuildingStructureId(String buildingStructureId) {
		this.buildingStructureId = buildingStructureId;
	}    
    
    public String getFunctionalId() {
		return functionalId;
	}

	public void setFunctionalId(String functionalId) {
		this.functionalId = functionalId;
	}

	public Page getPage() {
		return page;
	}

	public void setPage(Page page) {
		this.page = page;
	}



	public Property getProperty() {
		return property;
	}

	public void setProperty(Property property) {
		this.property = property;
	}

	public String getComponentPropertyId() {
        return componentPropertyId;
    }

    public void setComponentPropertyId(String componentPropertyId) {
        this.componentPropertyId = componentPropertyId;
    }

    public String getComponentId() {
        return componentId;
    }

    public void setComponentId(String componentId) {
        this.componentId = componentId;
    }

    public String getPropertyId() {
        return propertyId;
    }

    public void setPropertyId(String propertyId) {
        this.propertyId = propertyId;
    }

    public Double getPropertyNumber() {
        return propertyNumber;
    }

    public void setPropertyNumber(Double propertyNumber) {
        this.propertyNumber = propertyNumber;
    }

    public String getPropertyArea() {
        return propertyArea;
    }

    public void setPropertyArea(String propertyArea) {
        this.propertyArea = propertyArea;
    }

	public String getFunctionalName() {
		return functionalName;
	}

	public void setFunctionalName(String functionalName) {
		this.functionalName = functionalName;
	}

	public String getPropertyName() {
		return propertyName;
	}

	public void setPropertyName(String propertyName) {
		this.propertyName = propertyName;
	}

	public Functional getFunctionals() {
		return functionals;
	}

	public void setFunctionals(Functional functionals) {
		this.functionals = functionals;
	}

	public List<PropertyType> getPropertytype() {
		return propertytype;
	}

	public void setPropertytype(List<PropertyType> propertytype) {
		this.propertytype = propertytype;
	}
    
}