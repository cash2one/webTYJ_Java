package com.flf.entity;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement(name = "BankDeposit")//银行存单表
public class BankDeposit {

	private String bankDepositId;//银行存单id
	private String bankDepositNum;//银行存单号
	private double sum;//金额
	private String accountRecordId;//交账记录id
	
	public String getBankDepositId() {
		return bankDepositId;
	}
	public void setBankDepositId(String bankDepositId) {
		this.bankDepositId = bankDepositId;
	}
	public String getBankDepositNum() {
		return bankDepositNum;
	}
	public void setBankDepositNum(String bankDepositNum) {
		this.bankDepositNum = bankDepositNum;
	}
	public double getSum() {
		return sum;
	}
	public void setSum(double sum) {
		this.sum = sum;
	}
	public String getAccountRecordId() {
		return accountRecordId;
	}
	public void setAccountRecordId(String accountRecordId) {
		this.accountRecordId = accountRecordId;
	}
	
}
