package com.flf.entity;

import javax.xml.bind.annotation.XmlRootElement;
//房屋入伙物品表
@XmlRootElement(name = "HouseArticle")
public class HouseArticle {
	private String articleId;//物品主键
	
	private String articleName;//物品名称
	
	private int quantity;//数量
	
	private String propertyId;//资产id
	
	private String houseId;//房屋id
	
	private Byte articleState;//物品领取状态(0未领取1已领取)

	public String getArticleId() {
		return articleId;
	}

	public void setArticleId(String articleId) {
		this.articleId = articleId;
	}

	public String getArticleName() {
		return articleName;
	}

	public void setArticleName(String articleName) {
		this.articleName = articleName;
	}



	public int getQuantity() {
		return quantity;
	}

	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}

	public String getPropertyId() {
		return propertyId;
	}

	public void setPropertyId(String propertyId) {
		this.propertyId = propertyId;
	}

	public String getHouseId() {
		return houseId;
	}

	public void setHouseId(String houseId) {
		this.houseId = houseId;
	}

	public Byte getArticleState() {
		return articleState;
	}

	public void setArticleState(Byte articleState) {
		this.articleState = articleState;
	}
}
