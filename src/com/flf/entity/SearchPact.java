package com.flf.entity;

import java.util.Date;

import javax.xml.bind.annotation.XmlRootElement;
@XmlRootElement(name = "SearchPact") 
public class SearchPact {

    private Date minStaTime;//最小结束时间
    
    private Date maxStaTime;//最大结束时间
    
    private String projectId;//项目名称
    
    private String pactName;//合同名称
    
    private Byte state; // 合同状态
    
    private Page page ; 
    
    public Page getPage() {
		return page;
	}

	public void setPage(Page page) {
		this.page = page;
	}

	public Byte getState() {
		return state;
	}

	public void setState(Byte state) {
		this.state = state;
	}

	public Date getMinStaTime() {
		return minStaTime;
	}

	public void setMinStaTime(Date minStaTime) {
		this.minStaTime = minStaTime;
	}

	public Date getMaxStaTime() {
		return maxStaTime;
	}

	public void setMaxStaTime(Date maxStaTime) {
		this.maxStaTime = maxStaTime;
	}


	public String getProjectId() {
		return projectId;
	}

	public void setProjectId(String projectId) {
		this.projectId = projectId;
	}

	public String getPactName() {
		return pactName;
	}

	public void setPactName(String pactName) {
		this.pactName = pactName;
	}

	
	
}
