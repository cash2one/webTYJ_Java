package com.flf.entity;

import javax.xml.bind.annotation.XmlRootElement;

/**
 * 装修巡检结果记录
 * @author Administrator
 *
 */
@XmlRootElement(name = "RenovationInspectionRecords")
public class RenovationInspectionRecords {
	
	private String recordId;   //装修巡检记录id
	
	private String orderId;   //关联工单id
	
	private String inspectId;   //巡检项id
	
	private String inspectName;   //巡检项名称
	
	private Integer normal;   //巡检项是否正常
	
	private Integer conduct;   //巡检项是否进行
	
	private String remarks;   //备注说明

	public String getRecordId() {
		return recordId;
	}

	public void setRecordId(String recordId) {
		this.recordId = recordId;
	}

	public String getOrderId() {
		return orderId;
	}

	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}

	public String getInspectId() {
		return inspectId;
	}

	public void setInspectId(String inspectId) {
		this.inspectId = inspectId;
	}

	public String getInspectName() {
		return inspectName;
	}

	public void setInspectName(String inspectName) {
		this.inspectName = inspectName;
	}

	public Integer getNormal() {
		return normal;
	}

	public void setNormal(Integer normal) {
		this.normal = normal;
	}
	
	public Integer getConduct() {
		return conduct;
	}

	public void setConduct(Integer conduct) {
		this.conduct = conduct;
	}

	public String getRemarks() {
		return remarks;
	}

	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
}
