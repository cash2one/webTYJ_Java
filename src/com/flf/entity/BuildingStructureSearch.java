package com.flf.entity;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement(name = "BuildingStructureSearch")
public class BuildingStructureSearch {

	private String projectId;  //项目id
	
	private Byte isBindingAssets;// 是否资产绑定
	
	private String name; //客户姓名
	
	private String schemeName; //方案名称	
	
	//新增企业客户名称
	private String enterpriseName;//企业名称
	
	private String custType;//人员资产关系类型
	
	private Integer state;//人员资产关系启用状态
	
	private String fullName;//建筑全名称
	
	private String userId;
	
	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	public String getCustType() {
		return custType;
	}

	public void setCustType(String custType) {
		this.custType = custType;
	}

	public Integer getState() {
		return state;
	}

	public void setState(Integer state) {
		this.state = state;
	}


    public String getEnterpriseName() {
		return enterpriseName;
	}

	public void setEnterpriseName(String enterpriseName) {
		this.enterpriseName = enterpriseName;
	}


	public String getSchemeName() {
		return schemeName;
	}

	public void setSchemeName(String schemeName) {
		this.schemeName = schemeName;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getProjectId() {
		return projectId;
	}

	public void setProjectId(String projectId) {
		this.projectId = projectId;
	}

	public Byte getIsBindingAssets() {
		return isBindingAssets;
	}

	public void setIsBindingAssets(Byte isBindingAssets) {
		this.isBindingAssets = isBindingAssets;
	}
	
	
}
