package com.flf.entity;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement(name = "TeamPermissions")  // 团队权限表
public class TeamPermissions {  
    private String teamPermissionsId;  // 团队权限id

    private String projectId; // 项目id

    private String professionalLineId;  // 专业线id

    private String staffId;  // 人员id

    private String teamworkId;  // 团队id
    
    private String staffName; //负责人名称
    
    private String corePositionId;//核心团队id
    
    private CorePosition corePosition;//关联核心岗位
    

    public String getStaffName() {
		return staffName;
	}

	public void setStaffName(String staffName) {
		this.staffName = staffName;
	}

	public String getTeamPermissionsId() {
        return teamPermissionsId;
    }

    public void setTeamPermissionsId(String teamPermissionsId) {
        this.teamPermissionsId = teamPermissionsId;
    }

    public String getProjectId() {
        return projectId;
    }

    public void setProjectId(String projectId) {
        this.projectId = projectId;
    }

    public String getProfessionalLineId() {
        return professionalLineId;
    }

    public void setProfessionalLineId(String professionalLineId) {
        this.professionalLineId = professionalLineId;
    }

    public String getStaffId() {
        return staffId;
    }

    public void setStaffId(String staffId) {
        this.staffId = staffId;
    }

    public String getTeamworkId() {
        return teamworkId;
    }

    public void setTeamworkId(String teamworkId) {
        this.teamworkId = teamworkId;
    }

	public String getCorePositionId() {
		return corePositionId;
	}

	public void setCorePositionId(String corePositionId) {
		this.corePositionId = corePositionId;
	}

	public CorePosition getCorePosition() {
		return corePosition;
	}

	public void setCorePosition(CorePosition corePosition) {
		this.corePosition = corePosition;
	}
}